#define R(i) gpr(i)
#define Mr vaddr_read
#define Mw vaddr_write

enum
{
  TYPE_R, // f,        rs2, rs1, f,      rd,    op
  TYPE_I, // imm,           rs1, f,      rd,    op
  TYPE_S, // imm,      rs2, rs1, f,      imm,   op
  TYPE_B, // imm, imm, rs2, rs1, f, imm, imm,   op
  TYPE_U, // imm,                        rd,    op
  TYPE_J, // imm, imm, imm, imm,         rd,    op
  TYPE_N, // none
};

#define src1R(n)  \
  do              \
  {               \
    *src1 = R(n); \
  } while (0) //把cpu.gpr[n]赋给src1
#define src2R(n)  \
  do              \
  {               \
    *src2 = R(n); \
  } while (0)
#define destR(n) \
  do             \
  {              \
    *dest = n;   \
  } while (0)
#define src1I(i) \
  do             \
  {              \
    *src1 = i;   \
  } while (0)
#define src2I(i) \
  do             \
  {              \
    *src2 = i;   \
  } while (0)
#define destI(i) \
  do             \
  {              \
    *dest = i;   \
  } while (0)

static word_t immI(uint32_t i)
{
  return SEXT(BITS(i, 31, 20), 12);
}
static word_t immU(uint32_t i) { return SEXT(BITS(i, 31, 12), 20) << 12; }
static word_t immS(uint32_t i) { return (SEXT(BITS(i, 31, 25), 7) << 5) | BITS(i, 11, 7); }
static word_t immJ(uint32_t i) { return (SEXT(BITS(i, 31, 31), 1) << 20) | BITS(i, 30, 21) << 1 | BITS(i, 20, 20) << 11 | BITS(i, 19, 12) << 12; } //按J的imm排列
static word_t immB(uint32_t i) { return (SEXT(BITS(i, 31, 31), 1) << 12) | BITS(i, 30, 25) << 5 | BITS(i, 11, 8) << 1 | BITS(i, 7, 7) << 11; }

static void decode_operand(Decode *s, word_t *dest, word_t *src1, word_t *src2, int type)
{
  uint32_t i = s->isa.inst.val;
  int rd = BITS(i, 11, 7);   // i[11:7] //固定位置
  int rs1 = BITS(i, 19, 15); // i[19:15] //固定位置
  int rs2 = BITS(i, 24, 20); // i[24:20] //固定位置
  destR(rd);                 // *dest = rd = i[11:7]
  switch (type)
  {
  case TYPE_R:
    src1R(rs1);
    src2R(rs2);
    // printf("hsay: dest = %lx, src1 = %lx, src2 = %lx\n", *dest, *src1, *src2);
    break;
  case TYPE_I:
    src1R(rs1);     //*src1 = cpu.gpr[i[19:15]]
    src2I(immI(i)); //*src2 = { 52{signed}, i[31:20] }
    // printf("hsay: dest = %lx, src1 = %lx, src2 = %lx\n", *dest, *src1, *src2);
    break;
  case TYPE_S:
    destI(immS(i)); //*dest = { 52{signed}, i[31:25], i[11,7] }
    src1R(rs1);     //*src1 = cpu.gpr[i[19:15]]
    src2R(rs2);     //*src2 = cpu.gpr[i[24:20]]
    // printf("hsay: dest = %lx, src1 = %lx, src2 = %lx\n", *dest, *src1, *src2);
    break;
  case TYPE_B:
    destI(immB(i));
    src1R(rs1);
    src2R(rs2);
    // printf("hsay: dest = %lx, src1 = %lx, src2 = %lx\n", *dest, *src1, *src2);
    break;
  case TYPE_U:
    src1I(immU(i)); //*src1 = { 32{signed}, i[31:12], 12{0} }
    // printf("hsay: dest = %lx, src1 = %lx, src2 = %lx\n", *dest, *src1, *src2);
    break;
  case TYPE_J:
    s->dnpc = s->pc + immJ(i);
    // src1I(immJ(i));
    // s->dnpc = *src1;
    // printf("hsay: dest = %lx, src1 = %lx, src2 = %lx\n", *dest, *src1, *src2);
    break;
  case TYPE_N:
    // printf("hsay: dest = %lx, src1 = %lx, src2 = %lx\n", *dest, *src1, *src2);
    break;
  }
}

static int decode_exec(Decode *s)
{
  word_t dest = 0, src1 = 0, src2 = 0;
  s->dnpc = s->snpc; // dnpc指向也指向下条指令了//动态指令

#define INSTPAT_INST(s) ((s)->isa.inst.val) // isa.inst.val指令的值32bit
#define INSTPAT_MATCH(s, name, type, ... /* body */)             \
  {                                                              \
    decode_operand(s, &dest, &src1, &src2, concat(TYPE_, type)); \
    __VA_ARGS__;                                                 \
  }
  // printf("hsay: inst is %x\n", s->isa.inst.val);
  // printf("hsay: dnpc = %ld,= %lx\n",s->dnpc,s->dnpc);

  // 0_0000000110_000000000_00001_11011_11

  INSTPAT_START();
  /*         imm     rs2  rs1 func   rd   oprand     */
  INSTPAT("??????? ????? ????? ??? ????? 00101 11", auipc, U, R(dest) = src1 + s->pc); // 6个空格，32个0、1或？，一个\0//39
  INSTPAT("??????? ????? ????? ??? ????? 01101 11", lui, U, R(dest) = src1);

  INSTPAT("??????? ????? ????? 011 ????? 00000 11", ld, I, R(dest) = Mr(src1 + src2, 8));
  INSTPAT("??????? ????? ????? 100 ????? 00000 11", lbu, I, R(dest) = 0x000000ff & Mr(src1 + src2, 1));
  INSTPAT("??????? ????? ????? 010 ????? 00000 11", lw, I, R(dest) = SEXT(Mr(src1 + src2, 4), 32));
  INSTPAT("??????? ????? ????? 001 ????? 00000 11", lh, I, R(dest) = (int32_t)SEXT(Mr(src1 + src2, 2), 16));
  INSTPAT("??????? ????? ????? 101 ????? 00000 11", lhu, I, R(dest) = 0x0000ffff & Mr(src1 + src2, 2));
  INSTPAT("??????? ????? ????? 011 ????? 01000 11", sd, S, Mw(src1 + dest, 8, (uint64_t)src2));
  INSTPAT("??????? ????? ????? 010 ????? 01000 11", sw, S, Mw(src1 + dest, 4, (uint32_t)src2));
  INSTPAT("??????? ????? ????? 001 ????? 01000 11", sh, S, Mw(src1 + dest, 2, (uint16_t)src2));
  INSTPAT("??????? ????? ????? 000 ????? 01000 11", sb, S, Mw(src1 + dest, 1, (uint8_t)src2));

  INSTPAT("??????? ????? ????? ??? ????? 11011 11", jal, J, { R(dest) = s->pc + 4; });
  INSTPAT("??????? ????? ????? 000 ????? 11001 11", jalr, I, {s->dnpc = (src1 + SEXT(src2, 12))&0xfffffffffffffffe; R(dest) = s->pc + 4; });

  INSTPAT("0000000 ????? ????? 000 ????? 01100 11", add, R, R(dest) = src1 + src2);
  INSTPAT("??????? ????? ????? 000 ????? 00100 11", addi, I, R(dest) = src1 + src2);
  INSTPAT("0100000 ????? ????? 000 ????? 01100 11", sub, R, R(dest) = src1 - src2);
  INSTPAT("0000000 ????? ????? 000 ????? 01110 11", addw, R, R(dest) = (int32_t)(src1 + src2));
  INSTPAT("0100000 ????? ????? 000 ????? 01110 11", subw, R, R(dest) = (int32_t)(src1 - src2));
  INSTPAT("??????? ????? ????? 000 ????? 00110 11", addiw, I, R(dest) = SEXT((src1 + SEXT(src2, 12)), 32));
  INSTPAT("0000001 ????? ????? 000 ????? 01100 11", mul, R, R(dest) = src1 * src2);
  INSTPAT("0000001 ????? ????? 000 ????? 01110 11", mulw, R, R(dest) = (int32_t)SEXT(((int32_t)src1 * (int32_t)src2), 32));
  INSTPAT("0000001 ????? ????? 100 ????? 01110 11", divw, R, R(dest) = src1 / src2);
  INSTPAT("0000001 ????? ????? 110 ????? 01110 11", remw, R, R(dest) = src1 % src2);
  INSTPAT("??????? ????? ????? 011 ????? 00100 11", sltiu, I, R(dest) = (src1 < src2) ? 1 : 0);
  INSTPAT("??????? ????? ????? 011 ????? 01100 11", sltu, R, R(dest) = ((word_t)src1 < (word_t)src2) ? 1 : 0);
  INSTPAT("??????? ????? ????? 010 ????? 01100 11", slt, R, R(dest) = ((sword_t)src1 < (sword_t)src2) ? 1 : 0);

  INSTPAT("??????? ????? ????? 000 ????? 11000 11", beq, B, s->dnpc = ((sword_t)src1 == (sword_t)src2) ? s->pc + dest : s->snpc);
  INSTPAT("??????? ????? ????? 001 ????? 11000 11", bne, B, s->dnpc = ((sword_t)src1 != (sword_t)src2) ? s->pc + dest : s->snpc);
  INSTPAT("??????? ????? ????? 101 ????? 11000 11", bge, B, s->dnpc = ((sword_t)src1 >= (sword_t)src2) ? s->pc + dest : s->snpc);
  INSTPAT("??????? ????? ????? 111 ????? 11000 11", bgeu, B, s->dnpc = ((word_t)src1 >= (word_t)src2) ? s->pc + dest : s->snpc);
  INSTPAT("??????? ????? ????? 100 ????? 11000 11", blt, B, s->dnpc = ((sword_t)src1 < (sword_t)src2) ? s->pc + dest : s->snpc);
  INSTPAT("??????? ????? ????? 110 ????? 11000 11", bltu, B, s->dnpc = ((word_t)src1 < (word_t)src2) ? s->pc + dest : s->snpc);

  INSTPAT("000000? ????? ????? 001 ????? 00100 11", slli, I, R(dest) = (src1 << src2));
  INSTPAT("000000? ????? ????? 101 ????? 00100 11", srli, I, R(dest) = (word_t)src1 >> src2);
  INSTPAT("0000000 ????? ????? 101 ????? 01110 11", srlw, R, R(dest) = (int32_t)((uint32_t)src1 >> src2));
  INSTPAT("010000? ????? ????? 101 ????? 00100 11", srai, I, R(dest) = (sword_t)src1 >> (src2 - 1024));
  INSTPAT("0000000 ????? ????? 001 ????? 01110 11", sllw, R, R(dest) = (int32_t)((uint32_t)src1 << src2));
  INSTPAT("0000000 ????? ????? 001 ????? 00110 11", slliw, I, R(dest) = (int32_t)((uint32_t)src1 << src2));
  INSTPAT("0000000 ????? ????? 101 ????? 00110 11", srliw, I, R(dest) = (int32_t)((uint32_t)src1 >> src2));
  INSTPAT("0100000 ????? ????? 101 ????? 01110 11", sraw, R, R(dest) = (int32_t)((int32_t)src1 >> src2));
  INSTPAT("0100000 ????? ????? 101 ????? 00110 11", sraiw, I, R(dest) = (int32_t)((int32_t)src1 >> (src2 - 1024)));

  INSTPAT("??????? ????? ????? 111 ????? 00100 11", andi, I, R(dest) = src1 & src2);
  INSTPAT("0000000 ????? ????? 111 ????? 01100 11", and, R, R(dest) = src1 & src2);
  INSTPAT("0000000 ????? ????? 110 ????? 01100 11", or, R, R(dest) = src1 | src2);
  INSTPAT("??????? ????? ????? 100 ????? 00100 11", xori, I, R(dest) = src1 ^ src2);

  INSTPAT("0000000 00001 00000 000 00000 11100 11", ebreak, N, NEMUTRAP(s->pc, R(10))); // R(10) is $a0 // 取出$a0的值
  INSTPAT("??????? ????? ????? ??? ????? ????? ??", inv, N, INV(s->pc));
  INSTPAT_END();

  R(0) = 0; // reset $zero to 0

  return 0;
}

int isa_exec_once(Decode *s)
{
  s->isa.inst.val = inst_fetch(&s->snpc, 4); //取出snpc的地址//并且snpc指向下一条指令//静态指令//指令宽度为4字节（32bit）
  return decode_exec(s);
}
